package com.example;

public class Tool {

    private int ToolId;
    private String ToolName;

    public void setToolId(int ToolId) {
        this.ToolId = ToolId;
    }

    public int getToolId() {
        return this.ToolId;
    }

    public String getToolName() {
        return this.ToolName;
    }

    public void setToolName(String ToolName) {
        this.ToolName = ToolName;
    }
    
}
